	private static String valueCache=null;
	
	public static String getValuesAsString(){
		if(valueCache==null)  {
			StringBuffer sb = new StringBuffer();
			for (TimeZoneEnum item : values()){
				sb.append(item.timeZoneId.replace('_', ' '));
				sb.append(IFConsts.enumValueSplitChar);
			}
			
			valueCache = sb.substring(0, sb.length()-1-1);
		}
		
		return valueCache;
	}
		
